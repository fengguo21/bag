<p>Kính gửi:   Quý khách hàng,</p>
<p>Bixy xin chân thành cảm ơn Quý khách hàng đã ủng hộ và tin tưởng sử dụng sản phẩm của chúng tôi.   
Nhằm đảm bảo quyền lợi của Quý khách hàng, Bixy xin trân trọng thông báo đến quý khách hàng chính sách và điều kiện bảo hành đối với các sản phẩm của chúng tôi. Chính sách này chỉ áp dụng cho việc sửa chữa bảo hành đối với các sản phẩm điện thoại di động, máy tính bảng và dây đeo thông minh, các thiết bị phát wifi  được chúng tôi bán ra tại thị trường Viêt Nam. </p>
<p>Chính sách bảo hành này có hiệu lực từ ngày 1/8/2017</p>
<p>1. Chính sách bảo hành</p>
<ul>
	
	<li>a) 12(mười hai) tháng đối với thân máy, pin là 12(mười hai) tháng nếu pin đi liền với máy mà không tháo rời.</li>
	<li>b) 06(sáu) tháng đối với bộ sạc, cáp dữ liệu, pin rời và tai nghe.</li>
	<li>c) Trong thời hạn 15 ngày đầu tiên tính từ ngày người sử dụng mua sản phẩm nếu thân máy gặp phải những lỗi tính năng do phần cứng của nhà sản xuất, khách hàng sẽ được quyền đổi hàng 1 đổi 1.</li>
	<li>d) Quý khách lưu ý về việc bảo quản phiếu mua hàng + hoá đơn kèm số IMEI để đối chiếu khi cần thiết.</li>
</ul>
<p>2. Điều kiện bảo hành:</p>
<span>Chúng tôi sẽ từ chối sửa chữa miễn phí trong các trường hợp máy hỏng không do lỗi của nhà sản xuất như sau:</span>
<ul>
	<li>a) Sản phẩm có những phần bị hao mòn tự nhiên hoặc người sử dụng tự gây lên trong quá trình sử dụng như bị nứt, vỡ, mòn, xước, tróc sơn, đổi màu, hoen ố, hư hại bề mặt trong quá trình sử dụng như vỏ máy, vỏ pin, vỏ sạc, nắp pin, mặt bàn phím, mặt màn hình, ống kính camera và miếng dán decal.</li>
	<li>b) Người sử dụng máy không theo quy định trong sách hướng dẫn đi kèm hoặc sử dụng phụ kiện hoặc phần mềm không phù hợp với sản phẩm của chúng tôi</li>
	<li>c) Sản phẩm bị hết hạn bảo hành.</li>
	<li>d) Máy bị khoá máy hoặc khoá SIM.</li>
	<li>e) Sản phẩm bị virus từ việc truy cập bất hợp pháp của người sử dụng hoặc bên thứ ba đến các dịch vụ, các tài khoản, hệ thống máy tính hoặc mạng. Việc truy cập trái phép có thể xảy ra hack, tìm kiếm mật mã hoặc qua các phương tiện khác.</li>
	<li>f) Bixy sẽ không bảo đảm rằng bất cứ phần mềm nào cũng đáp ứng được yêu cầu của người sử dụng và hoạt động được với bất cứ các ứng dụng phần cứng hoặc phần mềm được cung cấp bởi bên thứ ba.</li>
	<li>g) Máy hoặc phụ kiện bị vô nước hoặc rơi va đập: có vết mốc, gỉ, ăn mòn, bị nứt, vỡ, gãy, tháo rời, biến dạng, cháy nổ, có chất lỏng, mùi khét, mùi lạ, có vật chất lạ tại một phần hoặc toàn bộ.</li>
	<li>h) Máy hoặc phụ kiện đã qua kiểm tra, sữa chữa, thay thế, điều chỉnh, thay đổi phần mềm, phần cứng bởi cá nhân hoặc đơn vị khác không được ủy quyền của Bixy: bị rách, mất hoặc bong tem niêm phong, mất ốc, mất linh kiện hoặc bị thay linh kiện loại khác.</li>
