	<li><span>1. Phạm vi áp dụng</span>
	<p>Áp dụng cho 63 tỉnh thành trên Toàn Quốc.</p>
	</li>
	<li><span>2. Miễn Phí</span>
		
		<p>Miễn phí đối với đơn hàng lớn hơn 1triệu VND, còn đối với đơn hàng dưới 1 triệu khách hàng tự trả tiền cước vận chuyển khi nhận hàng.</p>
		<p>Đối với các đơn hàng bán hàng online, quý khách có thể chọn hình thức nhận hàng và đồng kiểm với bên chuyển phát và trả tiền cước và tiền hàng hóa cho bên chuyển phát.</p>
	</li>
	<li><span>3. Thời gian nhận hàng</span>
	<p>Sau khi quý khách đã đặt hàng thành công, chúng tôi sẽ tiến hành xác nhận đơn hàng thông qua điện thoại, email và tin nhắn SMS.</p>
	<p>Sau khi xác nhận đơn hàng thành công, chúng tôi sẽ tiến hành đóng gói và chuyển hàng cho đối tác vận chuyển (Viettel Post, các hãng phương tiện vận chuyển). Nhân viên giao hàng sẽ liên lạc với quý khách để thống nhất thời gian giao hàng. </p>
	<p>Đối với đơn hàng làm theo yêu cầu thay đổi mẫu mã, in ấn sản phẩm thì thời gian giao hàng từ 10 đến đến 15 ngày.</p>
	<p><strong>* Lưu ý: </strong>Thời gian trên là thời gian dự kiến. Thời gian giao hàng chính thức có thể sớm hơn hoặc trễ hơn một vài ngày tùy theo một số điều kiện khách quan không thể kiểm soát được (ví dụ: thiên tai, lũ lụt, .). Trong một số trường hợp bất khả kháng, khách sẽ nhận hàng muộn hơn thời gian dự kiến từ 3 đến 5 ngày.</p>
